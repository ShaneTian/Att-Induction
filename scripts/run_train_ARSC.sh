#!/bin/sh
CUDA_VISIBLE_DEVICES=0 python3 -u ./train.py \
    --train_data ARSC_train \
    --test_data books.t2 --test_data books.t4 --test_data books.t5 \
    --test_data dvd.t2 --test_data dvd.t4 --test_data dvd.t5 \
