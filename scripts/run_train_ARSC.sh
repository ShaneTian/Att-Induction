    --test_data kitchen_housewares.t2 --test_data kitchen_housewares.t4 --test_data kitchen_housewares.t5 \
    -N 2 \
    -K 5 \
    -Q 5 \
    --encoder bert-base \
    --model att-induction \
    --optim adamw \
    --train_episodes 70000 \
    --val_steps 1000 \
    --max_length 512 \
    --hidden_size 768 \
    --induction_iters 3 \
    --n_heads 4 \
